const nomes = ['Chico', 'Maria', 'Rogrigo', "Gabriel"];

//Adicionando o item no final da lista com .push()

nomes.push('Tais');

console.log(nomes);

//removendo o ultimo nome da lista

nomes.pop();

console.log(nomes);

//Adicionando um item ao inicio da lista

nomes.unshift("Lila");

console.log(nomes);

//Removendo o primeiro nome da lisra

nomes.shift();

console.log(nomes);